public class VarTypes {
    public static void main(String[] args) {
        // Defining String variable
        String name = "Sai Ram";
        // Defining int variable
        int age = 27;
        // Defining float variable
        float gradePercentage = 96.60f;
        // Defininf char Variable
        char val = 'k';
        // Defining boolean variable
        boolean option = true;
        System.out.println(name);
        System.out.println(age);
        System.out.println(gradePercentage);
        System.out.println(val);
        System.out.println(option);
        
        // defining variale and assigning value in next line
        int myNum;
        myNum = 143;
        System.out.println(myNum);
        
        // making final value if another user want to modify also they can't modify
        final String Name;
        Name = "sai Karthikeyan";
        System.out.println(Name);
        name = "Balaji";
        System.out.println(name);
    }
}